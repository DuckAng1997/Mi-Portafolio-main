const state = {
  lang: localStorage.getItem("lang") || "es",
  theme: localStorage.getItem("theme") || "dark",
  rotatorIndex: 0,
  rotatorTimer: null
};

const body = document.body;
const navToggle = document.getElementById("nav-toggle");
const navMenu = document.getElementById("nav-menu");
const themeToggle = document.getElementById("theme-toggle");
const logo = document.getElementById("logo");
const rotator = document.getElementById("hero-rotator");
const sections = document.querySelectorAll("main section[id]");
const navLinks = document.querySelectorAll(".nav-link");
const revealItems = document.querySelectorAll(".reveal");
const counterItems = document.querySelectorAll(".stat-number");
const langButtons = document.querySelectorAll("[data-lang-trigger]");

function applyTheme() {
  body.classList.toggle("light-theme", state.theme === "light");
  themeToggle.innerHTML = state.theme === "light"
    ? '<i class="fa-solid fa-moon"></i>'
    : '<i class="fa-solid fa-sun"></i>';
  logo.src = state.theme === "light" ? "img/logon.png" : "img/logob.png";
  localStorage.setItem("theme", state.theme);
}

function applyTranslations() {
  document.documentElement.lang = state.lang;
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (translations[state.lang][key]) {
      element.textContent = translations[state.lang][key];
    }
  });

  langButtons.forEach((button) => {
    button.classList.toggle("active", button.dataset.langTrigger === state.lang);
  });

  startRotator(true);
  localStorage.setItem("lang", state.lang);
}

function startRotator(reset = false) {
  const titles = rotatingTitles[state.lang];
  if (reset) {
    state.rotatorIndex = 0;
  }

  clearInterval(state.rotatorTimer);
  rotator.textContent = titles[state.rotatorIndex];

  state.rotatorTimer = setInterval(() => {
    state.rotatorIndex = (state.rotatorIndex + 1) % titles.length;
    rotator.textContent = titles[state.rotatorIndex];
  }, 2400);
}

function setActiveLink() {
  const offset = window.scrollY + 120;

  sections.forEach((section) => {
    const top = section.offsetTop;
    const height = section.offsetHeight;
    const id = section.getAttribute("id");
    const link = document.querySelector(`.nav-link[href="#${id}"]`);

    if (!link) {
      return;
    }

    link.classList.toggle("active", offset >= top && offset < top + height);
  });
}

function animateCounter(entry) {
  const element = entry.target;
  const target = Number(element.dataset.count || 0);
  let current = 0;
  const increment = Math.max(1, Math.ceil(target / 30));

  const timer = setInterval(() => {
    current += increment;
    if (current >= target) {
      current = target;
      clearInterval(timer);
    }
    element.textContent = target === 100 ? `${current}%` : `${current}+`;
  }, 35);
}

const revealObserver = new IntersectionObserver((entries, observer) => {
  entries.forEach((entry) => {
    if (!entry.isIntersecting) {
      return;
    }

    entry.target.classList.add("is-visible");
    observer.unobserve(entry.target);
  });
}, { threshold: 0.18 });

const counterObserver = new IntersectionObserver((entries, observer) => {
  entries.forEach((entry) => {
    if (!entry.isIntersecting) {
      return;
    }

    animateCounter(entry);
    observer.unobserve(entry.target);
  });
}, { threshold: 0.65 });

navToggle.addEventListener("click", () => {
  navMenu.classList.toggle("is-open");
  body.classList.toggle("menu-open");
});

navLinks.forEach((link) => {
  link.addEventListener("click", () => {
    navMenu.classList.remove("is-open");
    body.classList.remove("menu-open");
  });
});

themeToggle.addEventListener("click", () => {
  state.theme = state.theme === "light" ? "dark" : "light";
  applyTheme();
});

langButtons.forEach((button) => {
  button.addEventListener("click", () => {
    state.lang = button.dataset.langTrigger;
    applyTranslations();
  });
});

window.addEventListener("scroll", setActiveLink);

revealItems.forEach((item) => revealObserver.observe(item));
counterItems.forEach((item) => counterObserver.observe(item));

applyTheme();
applyTranslations();
setActiveLink();
